

namespace MsgBoxEx
{
    /// <summary>
    /// Contains all possible values for the InformationBox icon.
    /// </summary>
    public enum MsgBoxIcon
    {
        /// <summary>
        /// Do not show an icon
        /// </summary>
        None,

        /// <summary>
        /// Error icon
        /// </summary>
        Error,

        /// <summary>
        /// Information icon
        /// </summary>
        Information,
        
        /// <summary>
        /// Question icon
        /// </summary>
        Question,

        /// <summary>
        /// Warning icon
        /// </summary>
        Warning,
    }
}
